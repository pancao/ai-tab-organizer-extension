  const PROVIDER_LABEL_KEYS = Object.freeze({
    siliconflow: "providerSiliconflow",
    dashscope: "providerDashscope",
    custom: "providerCustom"
  });

  function resolveUILanguage(input) {
    const value = String(input || "").trim().toLowerCase();
    return LANGUAGE_MAP.has(value) ? value : DEFAULT_UI_LANGUAGE;
  }

  function getLocaleTag(language) {
    return LANGUAGE_MAP.get(resolveUILanguage(language))?.localeTag || "zh-CN";
  }

  function getMessages(language) {
    return MESSAGES[resolveUILanguage(language)] || MESSAGES[DEFAULT_UI_LANGUAGE];
  }

  function interpolate(template, vars) {
    return String(template || "").replace(/\{(\w+)\}/g, (_match, key) => String(vars?.[key] ?? ""));
  }

  function t(language, key, vars) {
    const base = getMessages(DEFAULT_UI_LANGUAGE);
    const localized = getMessages(language);
    return interpolate(localized[key] ?? base[key] ?? key, vars);
  }

  function getLanguageOptions() {
    return SUPPORTED_UI_LANGUAGES.slice();
  }

  function getThemeOptions(language) {
    return THEME_COLOR_KEYS.map((key) => ({ key, label: t(language, `theme${capitalize(key)}`) }));
  }

  function getProviderLabel(providerId, language, fallbackLabel) {
    const key = PROVIDER_LABEL_KEYS[providerId];
    return key ? t(language, key) : fallbackLabel || providerId;
  }

  function getProviderApiKeyPlaceholder(providerId, language, fallbackLabel) {
    if (providerId === "custom") {
      return t(language, "customApiKeyPlaceholder");
    }

    const providerLabel = getProviderLabel(providerId, language, fallbackLabel);
    return t(language, "providerApiKeyPlaceholder", { provider: providerLabel });
  }

  async function getStoredLanguage(storageArea) {
    const storage = storageArea || root.chrome?.storage?.local;

    if (!storage?.get) {
      return DEFAULT_UI_LANGUAGE;
    }

    const stored = await storage.get([UI_LANGUAGE_STORAGE_KEY]);
    return resolveUILanguage(stored?.[UI_LANGUAGE_STORAGE_KEY]);
  }

  function capitalize(value) {
    return String(value || "").slice(0, 1).toUpperCase() + String(value || "").slice(1);
  }

  return Object.freeze({
    DEFAULT_UI_LANGUAGE,
    UI_LANGUAGE_STORAGE_KEY,
    getLanguageOptions,
    getLocaleTag,
    getMessages,
    getProviderApiKeyPlaceholder,
    getProviderLabel,
    getStoredLanguage,
    getThemeOptions,
    resolveUILanguage,
    t
  });
